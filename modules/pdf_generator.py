from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle
from reportlab.lib import colors
from reportlab.lib.styles import getSampleStyleSheet
from reportlab.lib.pagesizes import letter
from datetime import datetime


def generate_pdf(results, patient_name="Unknown", age="N/A", gender="N/A", report_types=None, filename="medical_report_result.pdf"):

    doc = SimpleDocTemplate(filename, pagesize=letter)

    styles = getSampleStyleSheet()

    elements = []

    # Title
    title = Paragraph("<b>AI-Based Medical Report Analysis Result</b>",styles["Title"])
    elements.append(title)

    elements.append(Spacer(1, 20))

    # ---------------- PATIENT DETAILS ----------------

    current_date = datetime.now().strftime("%d-%m-%Y")

   

    patient_data = [
    [Paragraph("Name", styles["Normal"]),Paragraph(str(patient_name), styles["Normal"])],
    [Paragraph("Age", styles["Normal"]),Paragraph(str(age), styles["Normal"])],
    [Paragraph("Gender", styles["Normal"]),Paragraph(str(gender), styles["Normal"])],
    [Paragraph("Date", styles["Normal"]),Paragraph(current_date, styles["Normal"])]
    ]

    patient_table = Table(patient_data, colWidths=[120, 200])

    patient_table.setStyle(TableStyle([
        ("GRID", (0,0), (-1,-1), 1, colors.black),
        ("BACKGROUND", (0,0), (0,-1), colors.lightgrey),
        ("FONTNAME", (0,0), (-1,-1), "Helvetica"),
        ("FONTSIZE", (0,0), (-1,-1), 10),
    ]))

    elements.append(Paragraph("<b>Patient Details</b>", styles["Heading2"]))
    elements.append(Spacer(1,10))
    elements.append(patient_table)

    elements.append(Spacer(1, 20))

    # ---------------- REPORT TYPE ----------------

    report_text = ", ".join(report_types) if report_types else "Not detected"

    elements.append(Paragraph("<b>Detected Report Type</b>",styles["Heading2"]))
    elements.append(Spacer(1,10))
    elements.append(Paragraph(str(report_text), styles["Normal"]))
    elements.append(Spacer(1, 20))

    # ---------------- RESULT TABLE ----------------

    elements.append(Paragraph("<b>Disease Detection Result</b>", styles["Heading2"]))
    elements.append(Spacer(1,10))

    table_data = [[
        Paragraph("Disease", styles["Normal"]),
        Paragraph("Doctor", styles["Normal"]),
        Paragraph("Recommendation", styles["Normal"])
    ]]
    for row in results:
        table_data.append([
        Paragraph(row["Disease"], styles["Normal"]),
        Paragraph(row["Doctor"], styles["Normal"]),
        Paragraph(row["Recommendation"], styles["Normal"])
        ])

    table = Table(table_data, colWidths=[140, 140, 220])

    table.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), colors.grey),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),

        ("GRID", (0, 0), (-1, -1), 1, colors.black),

        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),

        ("BACKGROUND", (0, 1), (-1, -1), colors.whitesmoke),
    ]))

    elements.append(table)

    elements.append(Spacer(1, 20))

    # ---------------- DISCLAIMER ----------------

    disclaimer = Paragraph(
        "<b>Disclaimer:</b> This report is generated by an AI-based system for educational purposes only. "
        "It is not a medical diagnosis.Please consult a qualified doctor.",
        styles["Normal"]
    )

    elements.append(disclaimer)

    # Build PDF
    doc.build(elements)

    return filename