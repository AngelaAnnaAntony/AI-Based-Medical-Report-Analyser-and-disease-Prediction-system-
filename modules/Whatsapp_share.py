import urllib.parse

def generate_whatsapp_link():
    message = """
Hello,
Here is my AI Medical Report Analysis result.
Please find the attached PDF report.
Generated using AI-Based Medical Report Analyser.
"""
    encoded_message = urllib.parse.quote(message)
    link = f"https://wa.me/?text={encoded_message}"
    return link

def generate_result_text(results):
    text = "🧾 AI Medical Report Result:\n\n"

    for r in results:
        text += f"🔹 Disease: {r['Disease']}\n"
        text += f"👨‍⚕️ Doctor: {r['Doctor']}\n"
        text += f"💡 Advice: {r['Recommendation']}\n\n"

    text += "⚠️ Not a medical diagnosis. Please consult a doctor."
    text += "\n\nGenerated by AI Medical Report Analyzer"

    return text


def generate_whatsapp_link_with_results(results):
    result_text = generate_result_text(results)

    # Optional: limit length
    result_text = result_text[:1500]

    encoded_msg = urllib.parse.quote(result_text)

    whatsapp_url = f"https://wa.me/?text={encoded_msg}"

    return whatsapp_url
